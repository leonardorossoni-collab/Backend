function min(a, b) {

    if (a < b) {
        return a;
    } else {
        return b;
    }

}

console.log(min(7, 5));
console.log(min(10, 8 ));

const minArrow = (a, b) => {

    if (a < b) {
        return a;
    } else {
        return b;
    }

}

console.log(minArrow(8, 5));
console.log(minArrow(3, 10));
